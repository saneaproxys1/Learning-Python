#Задача 14: Поиск и замена подстроки в строке с использованием цикла
# Получаем строку от пользователя
input_string = input("Введите строку: ")
# Получаем подстроки для замены от пользователя
old_substring = input("Введите подстроку для замены: ")
new_substring = input("Введите новую подстроку: ")
# Переменная для хранения измененной строки
modified_string = ""
# Ищем и заменяем подстроку с использованием цикла
i = 0
while i < len(input_string):
    if input_string[i:i + len(old_substring)] == old_substring:
        modified_string += new_substring
        i += len(old_substring)
    else:
        modified_string += input_string[i]
        i += 1
print(f"Измененная строка: {modified_string}")
